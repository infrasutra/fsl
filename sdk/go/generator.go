type Generator struct{}

func New() *Generator {
	return &Generator{}
}

func (g *Generator) Language() string {
	return "go"
}

func (g *Generator) FileExtension() string {
	return ".go"
}

func (g *Generator) Generate(schemas []*parser.CompiledSchema, config sdk.GeneratorConfig) (*sdk.GeneratedSDK, error) {
	output := &sdk.GeneratedSDK{
		Language:   "go",
		Files:      make(map[string]string),
		EntryPoint: "client.go",
	}

	models, err := g.generateModels(schemas, config)
	if err != nil {
		return nil, fmt.Errorf("failed to generate models: %w", err)
	}
	output.Files["models.go"] = models

	if config.IncludeClient {
		client, err := g.generateClient(schemas, config)
		if err != nil {
			return nil, fmt.Errorf("failed to generate client: %w", err)
		}
		output.Files["client.go"] = client
	}

	return output, nil
}

