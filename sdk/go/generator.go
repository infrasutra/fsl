func (g *Generator) mapFieldType(schema *parser.CompiledSchema, field *parser.CompiledField, inputMode, forceOptional, component bool) string {
	baseType := g.mapBaseType(schema, field, inputMode, component)

	if len(field.Slices) > 0 {
		baseType = "[]" + g.sliceUnionTypeName(schema, field)
	}

	if field.Array {
		if strings.HasPrefix(baseType, "[]") {
			if forceOptional || (!field.ArrayReq && !field.Required) {
				return "*" + baseType
			}
			return baseType
		}

		arrayType := "[]" + baseType
		if forceOptional || (!field.ArrayReq && !field.Required) {
			return "*" + arrayType
		}
		return arrayType
	}

	if forceOptional || (!field.Required && !field.ArrayReq) {
		return pointerType(baseType)
	}

	return baseType
}

func pointerType(baseType string) string {
	if strings.HasPrefix(baseType, "*") {
		return baseType
	}
	return "*" + baseType
}

func (g *Generator) mapBaseType(schema *parser.CompiledSchema, field *parser.CompiledField, inputMode, component bool) string {
	if len(field.Slices) > 0 {
		return g.sliceUnionTypeName(schema, field)
	}

	if field.IsRelation {
		if inputMode {
			return "string"
		}
		return ToPascalCase(field.RelationTo)
	}

	if field.Type == "Enum" && len(field.InlineEnum) > 0 {
		if component {
			return "string"
		}
		return g.inlineEnumTypeName(schema, field)
	}

	if mapped, ok := TypeMapping[field.Type]; ok {
		if field.Type == "Enum" {
			return ToPascalCase(field.Type)
		}
		return mapped
	}

	for _, enum := range schema.Enums {
		if enum.Name == field.Type {
			return ToPascalCase(enum.Name)
		}
	}

	return ToPascalCase(field.Type)
}

func (g *Generator) inlineEnumTypeName(schema *parser.CompiledSchema, field *parser.CompiledField) string {
	return ToPascalCase(schema.Name) + ToPascalCase(field.Name)
}

func (g *Generator) sliceUnionTypeName(schema *parser.CompiledSchema, field *parser.CompiledField) string {
	return ToPascalCase(schema.Name) + ToPascalCase(field.Name) + "Slice"
}

