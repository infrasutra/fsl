func (g *Generator) generateEnumType(enum *parser.CompiledEnum) string {
	var buf bytes.Buffer

	typeName := ToPascalCase(enum.Name)
	buf.WriteString(fmt.Sprintf("type %s string\n\n", typeName))
	buf.WriteString("const (\n")
	for _, value := range enum.Values {
		constName := typeName + sanitizeIdentifier(value)
		buf.WriteString(fmt.Sprintf("\t%s %s = %q\n", constName, typeName, value))
	}
	buf.WriteString(")\n")

	return buf.String()
}

func (g *Generator) generateInlineEnums(schema *parser.CompiledSchema) string {
	var buf bytes.Buffer

	for _, field := range schema.Fields {
		if field.Type != "Enum" || len(field.InlineEnum) == 0 {
			continue
		}

		typeName := g.inlineEnumTypeName(schema, &field)
		buf.WriteString(fmt.Sprintf("type %s string\n\n", typeName))
		buf.WriteString("const (\n")
		for _, value := range field.InlineEnum {
			buf.WriteString(fmt.Sprintf("\t%s%s %s = %q\n", typeName, sanitizeIdentifier(value), typeName, value))
		}
		buf.WriteString(")\n\n")
	}

	return buf.String()
}

func (g *Generator) generateSliceSupportTypes(schema *parser.CompiledSchema) string {
	var buf bytes.Buffer

	componentByName := make(map[string]*parser.CompiledComponent, len(schema.Components))
	for i := range schema.Components {
		component := &schema.Components[i]
		componentByName[component.Name] = component
	}

	emittedComponents := map[string]bool{}

	for _, field := range schema.Fields {
		if len(field.Slices) == 0 {
			continue
		}

		for _, slice := range field.Slices {
			component, ok := componentByName[slice.Schema]
			if !ok {
				continue
			}

			componentTypeName := g.sliceComponentTypeName(schema, slice.Schema)
			if emittedComponents[componentTypeName] {
				continue
			}

			emittedComponents[componentTypeName] = true
			buf.WriteString(fmt.Sprintf("type %s struct {\n", componentTypeName))
			for _, componentField := range component.Fields {
				fieldName := ToPascalCase(componentField.Name)
				fieldType := g.mapFieldType(schema, &componentField, false, false, false)
				tag := "`json:\"" + componentField.Name
				if !componentField.Required && !componentField.ArrayReq {
					tag += ",omitempty"
				}
				tag += "\"`"
				buf.WriteString(fmt.Sprintf("\t%s %s %s\n", fieldName, fieldType, tag))
			}
			buf.WriteString("}\n\n")
		}

		unionTypeName := g.sliceUnionTypeName(schema, &field)
		buf.WriteString(fmt.Sprintf("type %s struct {\n", unionTypeName))
		buf.WriteString("\tType string `json:\"type\"`\n")
		buf.WriteString("\tVariation *string `json:\"variation,omitempty\"`\n")
		buf.WriteString("\tData map[string]any `json:\"data\"`\n")
		buf.WriteString("}\n\n")
	}

	return buf.String()
}

